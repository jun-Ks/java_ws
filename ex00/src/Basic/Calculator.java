package Basic;

public class Calculator {
    public int plus(int n1, int n2) {
        int result = n1 + n2;
        return result;
    }
    public int minus(int n1, int n2){
        int result = n1 - n2;
        return result;
    }
    public int multipl(int n1, int n2){
        int result = n1 * n2;
        return result;
    }
    public int division(int n1, int n2){
        int result = n1 / n2;
        return result;
    }
}
