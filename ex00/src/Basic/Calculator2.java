package Basic;

public class Calculator2{
        public static int plus(int n1, int n2) {
            int result = n1 + n2;
            return result;
        }
        public static int minus(int n1, int n2){
            int result = n1 - n2;
            return result;
        }
        public static int multipl(int n1, int n2){
            int result = n1 * n2;
            return result;
        }
        public static double division(double n1, double n2){
            double result = n1 / n2;
            return result;
        }
}
